# თქვენი დავალებაა ბოლო 4 ციფრის გარდა ყველა ასო/რიცხვი გადააქციო #-ად
# "4556364607935616" --> "############5616"
#      "64607935616" -->      "#######5616"
#                "1" -->                "1"
#                 "" -->                 ""


def task10(ran):
    res = ""

    if len(ran) <= 4:
        return ran
    
    res += "#"*(len(ran) - 4) + ran[-4:]

    return res

task10()